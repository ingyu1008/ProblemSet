int yut2int(const std::string& s) {
    int cnt = 0;
    for (int i = 0; i < s.size(); i++) {
        cnt += (s[i] == '0') ? 1 : 0;
    }

    switch (cnt) {
    case 0: return 4;
    case 1: return 1;
    case 2: return 2;
    case 3: return 3;
    default: return 5;
    }
}

